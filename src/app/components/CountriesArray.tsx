export default function CountrySelect({
  setCountry,
}: {
  setCountry: (countryId: number, countryName: string) => void;
}) {
  const [selectedCountryId, setSelectedCountryId] = useState<
    string | undefined
  >();

  const handleSelectCountry = (value: string) => {
    const selectedCountry = CountriesArray.find(
      (c) => c.id.toString() === value
    );
    if (selectedCountry) {
      setSelectedCountryId(value);
      setCountry(selectedCountry.id, selectedCountry.name);
    }
  };

  return (
    <Select
      onValueChange={handleSelectCountry}
      value={selectedCountryId}
      required
    >
      <SelectTrigger className='w-full'>
        <SelectValue placeholder='Bir ülke seç' />
      </SelectTrigger>
      <SelectContent>
        <SelectGroup>
          <SelectLabel>Tüm Ülkeler</SelectLabel>
          {CountriesArray.sort((a, b) => a.name.localeCompare(b.name)).map(
            (country) => (
              <SelectItem
                key={country.id}
                value={country.id.toString()}
                className='flex items-center'
              >
                <span className='mr-2'>{country.emoji}</span>
                {country.name}
              </SelectItem>
            )
          )}
        </SelectGroup>
      </SelectContent>
    </Select>
  );
}
